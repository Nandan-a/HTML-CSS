function add(a,b){
    return a+b
}
function sub(a,b){
    return a-b
}
function mul(a,b){
    return a*b
}
function div(a,b){
    return a/b
}
function square(a){
    return a**2
}
function min(a,b){
    return(Math.min(a,b))
}
function max(a,b){
    return(Math.max(a,b))
}
exports.add = add
exports.sub = sub
exports.mul = mul
exports.div = div
exports.square = square
exports.min = min
exports.max = max